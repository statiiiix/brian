// Build the Supabase Edge Function artifact: bundle OUR code into one ESM
// file (Deno cannot resolve the repo's Node-style ".js" relative imports),
// keep npm deps external, and generate a deno.json import map pinning each
// dep to the exact installed version via npm: specifiers.
import { build } from "esbuild";
import { createHash } from "node:crypto";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const serverDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outDir = path.resolve(serverDir, "../supabase/functions/brian");
mkdirSync(outDir, { recursive: true });

// Runtime deps reachable from the edge entry. @hono/node-server is Node-only
// and deliberately absent.
const deps = [
  "hono",
  "@hono/mcp",
  "@modelcontextprotocol/sdk",
  "pg",
  "openai",
  "bcryptjs",
  "jsonwebtoken",
  "jose",
  "zod",
];

const version = (dep) =>
  JSON.parse(readFileSync(path.join(serverDir, "node_modules", dep, "package.json"), "utf8")).version;

const imports = {};
for (const dep of deps) {
  const v = version(dep);
  imports[dep] = `npm:${dep}@${v}`;
  imports[`${dep}/`] = `npm:/${dep}@${v}/`; // subpath imports (e.g. sdk/server/mcp.js)
}

// A content-derived marker keeps the checked-in artifact reproducible while
// still giving operators an exact identifier for the deployed bundle.
const buildIdPlaceholder = "brian-edge-build-id-placeholder";
const result = await build({
  entryPoints: [path.join(serverDir, "src/edge/entry.ts")],
  bundle: true,
  format: "esm",
  platform: "node",
  target: "esnext",
  packages: "external",
  define: { __BUILD_ID__: JSON.stringify(buildIdPlaceholder) },
  minify: true,     // artifact is generated + shipped inline to the deploy API
  keepNames: true,  // error classes are matched by name in logs
  sourcemap: false,
  write: false,
  outfile: path.join(outDir, "index.js"),
  banner: { js: "// AUTO-GENERATED by `npm run edge:build` — do not edit." },
});

const importMap = JSON.stringify({ imports }, null, 2) + "\n";
const placeholderBundle = result.outputFiles[0].text;
const buildId = createHash("sha256")
  .update(placeholderBundle)
  .update(importMap)
  .digest("hex")
  .slice(0, 16);
const bundle = placeholderBundle.replaceAll(buildIdPlaceholder, buildId);
writeFileSync(path.join(outDir, "index.js"), bundle);
writeFileSync(path.join(outDir, "deno.json"), importMap);
console.log(`bundled → ${path.relative(process.cwd(), path.join(outDir, "index.js"))}`);
